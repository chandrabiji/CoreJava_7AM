package operators;

public class AssignmentOpertors {

	public static void main(String[] args) {
		int a = 10;
		System.out.println("A = "+a);
		a +=2;//a = a+2; 10+2=12
		System.out.println(a);//12
		a -=2;//a = a-2=12-2=10
		System.out.println(a);//10
		a *=2;
		System.out.println(a);//20
		a /=2;
		System.out.println(a);//10
		
	}

}
